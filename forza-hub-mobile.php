					<h3 class="itemSection">The Project</h3>
					<p class="itemText">I started this project as I have been a fan of the video-game franchise Forza for quite some years now, and I felt that they were missing out by not having a mobile application where you can access their content. They currently have an application, named Forza Hub, which is available on Xbox and Windows 10.</p>
					<p class="itemText">Forza Hub allows you to access content related to the game series without having to go to their website. This includes articles, galleries, and most importantly access to redeem in-game rewards. These rewards can be redeemed weekly, but it must be done through the website or the Forza Hub application. There is currently no way to do it inside the Forza games.</p>
					<p class="itemText">The goal of this project was to create a design proposal of a Forza Hub mobile app that had the core functionality of the console and desktop applications while remaining easy to use and optimised for the mobile platform.</p>
					<p class="itemText">Please note that this design proposal is unsolicited and does not reflect the views and/or ideas of Turn 10 and Microsoft.</p>

					<h3 class="itemSection">Research</h3>
					<p class="itemText">In order to get a better idea of the potential users and their needs, I conducted a survey. This survey was posted on the official Forza Motorsport forums and on Reddit in the subreddit r/Forza. I received 25 responses to this survey during the 2 weeks it was open.</p>
					<p class="itemText">From these survey responses, I could see that the average participant is a 25-34-year-old male living in the US. He has an iPhone and visits the Forza Motorsport website weekly primarily to catch up on the latest news and to redeem rewards.</p>
					<p class="itemText">Next, I asked the survey participants to prioritise six different suggestions for what the application could include. Since I already knew that the 'Rewards' section of the app had to be included, this was not mentioned in those six options.</p>
					<p class="itemText">These were the six options: Articles/News, Rivals (a game mode where you compete against other players for the fastest lap times), Game statistics, Photo gallery, My garage, and Achievements.</p>
					<p class="itemText">In the end, I looked at the average priority given and selected the top three: Game statistics, Photo gallery, and Articles/News. Now I was ready to start designing the application.</p>

					<h3 class="itemSection">Colours &amp; Fonts</h3>
					<p class="itemText">When I began this project, I had a good idea of what the final product could look like. I decided to use the colour scheme used on the Forza Motorsport website, as I wanted to keep a familiarity between the website and the mobile version. In addition to the colour scheme below, I have also used a few other colours for some elements.</p>
					<img src="img/portfolio/forza-hub-mobile/forza-hub-mobile-colours.jpg" class="itemImage itemImageMargin" alt="Forza Hub Mobile - Colour scheme">
					<p class="itemText">As for fonts, I decided to use the font-family <a href="https://www.google.com/fonts/specimen/Roboto" class="itemLink">Roboto</a>. I used this font as it is an easy-to-read sans-serif font and because I did not have access to the official font used on iOS (San Francisco). Ideally, the font used in the app should be San Francisco rather than Roboto.</p>

