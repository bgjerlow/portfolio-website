            <h3 class="itemSection">Challenges &amp; Goals</h3>
            <p class="itemText">While SPOT is attended by a large number of people every year, our desk and field research indicated that a large part of the citizens in Aarhus are not aware of the festival, despite it taking place just around the corner. Another challenge unique to the 2017 version of SPOT, is how they can attract the international audience coming to Aarhus in connection with the city being the European Capital of Culture in 2017.</p>
            <p class="itemText">Therefore, this project focuses on answering the following problem statement: How can SPOT attract more audience, including people living in and around Aarhus as well as tourists that are visiting the city during the time of the festival?</p>
            <p class="itemText">Related to the problem statement, these are the sub-questions the project aims to answer:</p>
            <p class="itemText">- What functionalities can be included on a campaign site in order to appeal to the target audiences?</p>
            <p class="itemText">- What design and style decisions should be taken in order to transmit the atmosphere of the festival?</p>
            <p class="itemText">- What media, aside from a campaign website, could be used to create interest among both the local population and the international audience?</p>
            
            <h3 class="itemSection">The approach</h3>
            <p class="itemText">For a detailed description of this project and our approach, I recommend checking out the <a href="archive/spot-festival-report.pdf" class="itemLink">report we wrote in connection with this project.</a></p>
            
            <h3 class="itemSection">The Result</h3>
            <p class="itemText">Based on field and desk research, we designed and developed a campaign website for the 2017 edition of SPOT Festival. The campaign site includes basic information about the festival, a festival programme, accommodation information for tourists, as well as a page about volunteer opportunities for the festival. The final website can be seen <a href="http://project.bogjerlow.com/Project_3.2/" class="itemLink">here.</a></p>
