            <h3 class="itemSection">Project Info</h3>
            <p class="itemText">For the first-year exam project of the Multimedia Design and Communication programme at Business Academy Aarhus, I and my team (Lavinia Avadanei, Lasma Pastare, and Jevgenijs Zaicevs) created a website and visual identity for While You Wait.</p>
            <p class="itemText">While You Wait is an application available on iOS and Android that helps restaurants, hairdressers and similar businesses with customer management by providing a platform for digital queueing. The timeframe for this project was 4 weeks.</p>
            <p class="itemText">During this project, my area of focus was the website design and development. Furthermore, I also took on the role as Scrum master, making sure that we utilised the Scrum framework throughout the project. The website was developed using HTML5, CSS3, JavaScript/jQuery, and PHP.</p>
