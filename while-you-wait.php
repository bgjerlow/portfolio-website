            <h3 class="itemSection">Challenges &amp; Goals</h3>
            <p class="itemText">The main challenge of this project was how a website could be used as a marketing tool in order to attract more customers in combination with a more accurate visual identity.</p>
            <p class="itemText">One goal of this project was to create a modern website that showcases the benefits of While You Wait, while at the same time building trust in the company among visitors. The second goal of this project was to create a new visual identity for While You Wait that more accurately depicted what service they provide.</p>
            
            <h3 class="itemSection">The Approach</h3>
            <p class="itemText">Having done field and desk research which gave us a better understanding of the client's product and their customers, we created personas based on the user interviews we conducted. The personas were used as a basis for design and content decisions for the new website.</p>
            <p class="itemText">During the process of planning/designing/developing the website, we conducted usability tests to ensure that the site was easy to navigate and that it fit the needs of the personas.</p>
            <p class="itemText">For a detailed description of this project and our approach, I recommend checking out <a href="archive/while-you-wait-report.pdf" class="itemLink">the report we wrote in connection with it</a>. The new visual identity we created can also be seen in this report.</p>

            <h3 class="itemSection">The Result</h3>
            <p class="itemText">Based on field and desk research, we designed and developed a  modern website for While You Wait. The website showcases both the benefits and features of the platform, as well as builds trust among visitors through thorough company information and customer reviews of the platform/service. The full website can be seen <a href="http://project.bogjerlow.com/Project_2.2_Exam/index.php" class="itemLink">here</a>.</p>
            <p class="itemText">In addition, the new visual identity created in this project more accurately depicts what service While You Wait offers.</p>
