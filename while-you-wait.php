            <h3 class="itemSection">The project</h3>
            <p class="itemText">For the first-year exam project of the Multimedia Design and Communication programme at Business Academy Aarhus, I and my team (Lavinia Avadanei, Lasma Pastare, and Jevgenijs Zaicevs) created a website and visual identity for While You Wait. While You Wait is an application available on iOS and Android that helps restaurants, hairdressers and similar businesses with customer management. The timeframe for this project was 4 weeks.</p>
            <p class="itemText">During this project, my area of focus was the website design and development. Furthermore, I also took on the role as Scrum master, making sure that we utilised the Scrum framework throughout the project. The website was developed using HTML5, CSS3, JavaScript/jQuery, and PHP.</p>
            <p class="itemText">For a more detailed description of this project and our process, I recommend checking out <a href="archive/while-you-wait-report.pdf" class="itemLink">the report we wrote in connection with it</a>. The visual identity we created can also be seen in this report.</p>

            <h3 class="itemSection">The Final Website</h3>
            <p class="itemText">Having done field and desk research which gave us a better understanding of the client's product and their customers, we created personas based on the user interviews we conducted. The personas were kept in mind when designing and creating content for the website. During the process of planning/designing/developing the website, we conducted usability tests to ensure that the site was easy to navigate and that it fit with our personas needs.</p>

            <p class="itemText">The goal of the website is to provide users with an overview of features and benefits of the application, while at the same time contributing to raising the level of trustworthiness of the company through recommendations, thorough contact information, and information about the company (their story, mission, vision and similar). A screenshot of the Home page can be seen below, and <a href="http://project.bogjerlow.com/Project_2.2_Exam/index.php" class="itemLink">the full website can be seen here</a>.</p>
